const mongoose = require('mongoose');

const chatSchema = new mongoose.Schema({
    msgContent: {
        type: String,
        required: true
    },
    sender: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true
    },
    receiver: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    },
    updatedAt: {
        type: Date,
        default: Date.now
    },
    readStatus: {
        type: Boolean,
        default: false
    }
});

module.exports = mongoose.model('Chat', chatSchema);